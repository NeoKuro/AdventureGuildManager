public enum EInteractableType
{
    Workstation = 0,        // Furniture in the guild that can be used to perform specific jobs
    Item        = 1,        // Items that can be collected (loot) or used (furniture - brooms/mops etc) to perform tasks
    Entity      = 2,        // Entities that can be interacted with (dead entities - loot, living NPCs - quests etc)
}